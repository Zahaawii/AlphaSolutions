package apiassignment.alphasolutions.model;


public class AwaitingEmployee {

    private int awaitingEmployeeID;
    private String awaitingEmployee_name;
    private String awaitingEmployee_email;
    private String awaitingEmployee_username;
    private String awaitingEmployee_password;
    private String awaitingEmployee_status;

    public AwaitingEmployee() {

    }



    public int getAwaitingEmployeeID() {
        return awaitingEmployeeID;
    }

    public void setAwaitingEmployeeID(int awaitingEmployeeID) {
        this.awaitingEmployeeID = awaitingEmployeeID;
    }

    public String getAwaitingEmployee_name() {
        return awaitingEmployee_name;
    }

    public void setAwaitingEmployee_name(String awaitingEmployee_name) {
        this.awaitingEmployee_name = awaitingEmployee_name;
    }

    public String getAwaitingEmployee_email() {
        return awaitingEmployee_email;
    }

    public void setAwaitingEmployee_email(String awaitingEmployee_email) {
        this.awaitingEmployee_email = awaitingEmployee_email;
    }

    public String getAwaitingEmployee_username() {
        return awaitingEmployee_username;
    }

    public void setAwaitingEmployee_username(String awaitingEmployee_username) {
        this.awaitingEmployee_username = awaitingEmployee_username;
    }

    public String getAwaitingEmployee_password() {
        return awaitingEmployee_password;
    }

    public void setAwaitingEmployee_password(String awaitingEmployee_password) {
        this.awaitingEmployee_password = awaitingEmployee_password;
    }

    public String getAwaitingEmployee_status() {
        return awaitingEmployee_status;
    }

    public void setAwaitingEmployee_status(String awaitingEmployee_status) {
        this.awaitingEmployee_status = awaitingEmployee_status;
    }
}
